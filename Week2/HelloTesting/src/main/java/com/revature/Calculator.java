public class Calculator {

	//we're going to make 3 simple methods that perform simple math calculations
	//remember, unit tests tend to test the smallest possible blocks of code (methods usually)
	
	//addition
	public int add(int x, int y) {
		return x + y;
	}
	
	//subtraction
	public int subtract(int x, int y) {
		return x - y;
	}
	
	//division
	public int divide(int x, int y) {
		return x / y;
	}
	
}
